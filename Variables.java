public class Variables {
    public static void main(String[] args) throws Exception {
        
        //Variable declaration
        int x = 1232;
        float y = 3.14f;
        boolean z = false;
        char symbol = '@';
        String name = "XY";
        
        //print in format of above variables
        System.out.printf("my number is %d\n", x); 
        System.out.printf("my float is %.2f\n", y); 
        System.out.printf("my boolean is %b\n", z); 
        System.out.printf("my char is %c\n", symbol); 
        System.out.printf("my string is %s\n", name); 
    }
}
